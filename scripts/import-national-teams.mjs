import { access, mkdir, writeFile } from "node:fs/promises";

const WIKIPEDIA_API =
  "https://it.wikipedia.org/w/api.php?action=parse&page=Codici_nazionali_della_FIFA&prop=text&format=json&origin=*";
const ASSET_DIR = new URL("../public/assets/national-teams/", import.meta.url);
const OUTPUT_FILE = new URL("../functions/_shared/national-teams.ts", import.meta.url);
const USER_AGENT = "SurvivorArenaAssetImporter/1.0 (https://github.com/aiprotips/survivor-arena)";

function sleep(ms) {
  return new Promise((resolve) => {
    setTimeout(resolve, ms);
  });
}

async function fileExists(fileUrl) {
  try {
    await access(fileUrl);
    return true;
  } catch {
    return false;
  }
}

function decodeHtml(value) {
  return value
    .replace(/&#(x?[0-9a-fA-F]+);/g, (_, code) =>
      String.fromCodePoint(code.startsWith("x") ? Number.parseInt(code.slice(1), 16) : Number.parseInt(code, 10)),
    )
    .replace(/&quot;/g, "\"")
    .replace(/&#039;/g, "'")
    .replace(/&amp;/g, "&")
    .replace(/&nbsp;|&#160;/g, " ")
    .replace(/<[^>]*>/g, "")
    .trim();
}

function normalizeSpaces(value) {
  return value.replace(/\s+/g, " ").trim();
}

function toHttpsUrl(value) {
  return value.startsWith("//") ? `https:${value}` : value;
}

function toLargerPngUrl(value) {
  return value.replace(/\/\d+px-([^/]+)$/u, "/120px-$1");
}

function parseTeams(html) {
  const start = html.indexOf("Codici dei membri FIFA");
  const end = html.indexOf("Codici dei non-membri FIFA");
  const memberSection = html.slice(start, end > start ? end : undefined);
  const rows = [
    ...memberSection.matchAll(
      /<tr>\s*<td>([\s\S]*?)<\/td>\s*<td>([\s\S]*?)<\/td>\s*<td>([\s\S]*?)<\/td>\s*<\/tr>/gu,
    ),
  ];

  return rows
    .map((row) => {
      const nameMatch = row[1].match(/<a[^>]*>([\s\S]*?)<\/a>/u);
      const code = decodeHtml(row[2]).replace(/\s+/g, "");
      const imageMatch = row[3].match(/src="([^"]+)"/u);

      if (!nameMatch || !/^[A-Z]{3}$/u.test(code) || !imageMatch) {
        return null;
      }

      const originalSourceUrl = toHttpsUrl(imageMatch[1]);

      return {
        code,
        fallbackSourceUrl: originalSourceUrl,
        logoUrl: `/assets/national-teams/${code.toLowerCase()}.png`,
        name: normalizeSpaces(decodeHtml(nameMatch[1])),
        sourceUrl: toLargerPngUrl(originalSourceUrl),
      };
    })
    .filter(Boolean);
}

async function downloadImage(urls) {
  let lastStatus = "";

  for (const url of urls) {
    let response;

    for (let attempt = 1; attempt <= 8; attempt += 1) {
      response = await fetch(url, {
        headers: {
          "User-Agent": USER_AGENT,
        },
      });

      if (response.ok || response.status !== 429) {
        break;
      }

      const retryAfter = Number(response.headers.get("retry-after"));
      await sleep((Number.isFinite(retryAfter) && retryAfter > 0 ? retryAfter * 1000 : 1500 * attempt) + 250);
    }

    if (response?.ok) {
      return Buffer.from(await response.arrayBuffer());
    }

    lastStatus = `${response?.status ?? "errore"}: ${url}`;
  }

  throw new Error(`Download non riuscito (${lastStatus})`);
}

function toTypeScript(teams) {
  const rows = teams
    .map(
      (team) =>
        `  { code: ${JSON.stringify(team.code)}, name: ${JSON.stringify(team.name)}, logoUrl: ${JSON.stringify(
          team.logoUrl,
        )} },`,
    )
    .join("\n");

  return `// Generated by scripts/import-national-teams.mjs\n// Source: https://it.wikipedia.org/wiki/Codici_nazionali_della_FIFA\n\nexport type NationalTeamSeed = {\n  code: string;\n  logoUrl: string;\n  name: string;\n};\n\nexport const nationalTeamSeeds: readonly NationalTeamSeed[] = [\n${rows}\n];\n`;
}

const response = await fetch(WIKIPEDIA_API, {
  headers: {
    "User-Agent": USER_AGENT,
  },
});

if (!response.ok) {
  throw new Error(`Wikipedia API non disponibile: ${response.status}`);
}

const payload = await response.json();
const teams = parseTeams(payload.parse.text["*"]);

if (teams.length !== 211) {
  throw new Error(`Numero nazionali inatteso: ${teams.length}. Atteso: 211.`);
}

await mkdir(ASSET_DIR, { recursive: true });

const shouldOverwrite = process.argv.includes("--force");

for (const team of teams) {
  const assetUrl = new URL(`${team.code.toLowerCase()}.png`, ASSET_DIR);

  if (shouldOverwrite || !(await fileExists(assetUrl))) {
    const image = await downloadImage([team.sourceUrl, team.fallbackSourceUrl]);
    await writeFile(assetUrl, image);
    await sleep(350);
  }
}

await writeFile(OUTPUT_FILE, toTypeScript(teams));

console.log(`Importate ${teams.length} nazionali in ${ASSET_DIR.pathname}`);
